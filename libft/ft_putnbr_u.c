#include "ft_printf.h"

int	ft_putnbr_u(unsigned int n)
{
	char	c;

	if (n >= 10)
	{
		ft_putnbr(n / 10);
	}
	c = n % 10 + '0';
	write(1, &c, 1);
	return (0);
}
